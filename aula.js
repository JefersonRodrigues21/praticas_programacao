const n1 = 10;
const n2 = 7;
const aprovado = true;
const minimo = 9;

// operadores
console.log('Operadores aritméticos');
console.log(n1 + n2);
console.log(n1 - n2);
console.log(n1 * n2);
console.log(n1 / n2);
console.log(n1 % n2);

console.log('Operadores Relacionais');
console.log(n1 == n2);
console.log(n1 != n2);
console.log(n1 < n2);
console.log(n1 > n2);
console.log(n1 <= n2);
console.log(n1 >= n2);

console.log('Operadores Lógicos');
console.log(aprovado);
console.log(!aprovado);
console.log(n2 >= 7 && n2 >= minimo); // e
console.log(n2 >= 7 || n2 >= minimo); // ou
